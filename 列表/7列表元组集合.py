a = (1,2,3,[5,6])
a[3][0] = 8
print(a)    # 元组不能修改，可以在里面嵌套列表，从而更改，但也只能改列表

a = [1,2,3,4,5,6,7,8]
b = a[::2]  # 第一个：表示全取，第二个接2表示相隔两个元素
c = a[::-1]  # 逆反,把列表的元素全部倒过来
print(b,c)

a ={1,11,2,1,3,4,2,2,4,1,3,4}
print(a)    # 集合自动省去重复的元素
print()

e = {1,2,3}
r = {1,4,5}
q = {1,6,7}
e.difference_update(r)  # 比较两个数，去掉重复的元素,加上update省去赋值变量步骤
print(e)
l = e.difference(q)     # 拿后面的值和前面的值对比，结果为前面的值
print(l)
m = e.union(r)  # 并集
print(m)
